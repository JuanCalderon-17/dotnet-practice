using System;

namespace dotnetpractice;

class Calculator
{
    public int Add(int a, int b)
    {
        return a + b;
    }

    public int Subtract (int a, int b)
    {
        return a - b;
    }
    
    public int Multiplication(int a, int b)
    {
        return a * b;
    }

    public var divisione(int a, int b)
    {
        return a / b;
    }
}